const addition = (a, b) => {
  return a + b;
};
const subtraction = (a, b) => {
  return a - b;
};
const multiplication = (a, b) => {
  return a * b;
};
const division = (a, b) => {
  return a / b;
};

console.log(
  `5+2 = ${addition(5, 2)}
9-4 = ${subtraction(10, 4)}
5*3 = ${multiplication(3, 5)}
8/2 = ${division(8, 2)}`
);
